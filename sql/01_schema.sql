CREATE TABLE USERS (
    USER_NO NUMBER NOT NULL,
    USER_ID VARCHAR2(50) NOT NULL,
    PASSWORD VARCHAR2(255) NOT NULL,
    USER_NAME VARCHAR2(50) NOT NULL,
    PHONE VARCHAR2(30),
    EMAIL VARCHAR2(100),
    ROLE VARCHAR2(20) DEFAULT 'OWNER' NOT NULL,
    STATUS VARCHAR2(20) DEFAULT 'ACTIVE' NOT NULL,
    CREATED_AT DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT PK_USERS PRIMARY KEY (USER_NO),
    CONSTRAINT UK_USERS_USER_ID UNIQUE (USER_ID),
    CONSTRAINT CK_USERS_ROLE CHECK (ROLE IN ('OWNER', 'ADMIN')),
    CONSTRAINT CK_USERS_STATUS CHECK (STATUS IN ('ACTIVE', 'INACTIVE'))
);

CREATE SEQUENCE SEQ_USER START WITH 1000 INCREMENT BY 1 NOCACHE NOCYCLE;

COMMENT ON TABLE USERS IS '사용자';
