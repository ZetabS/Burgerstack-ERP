CREATE TABLE INQUIRIES (
    INQUIRY_ID NUMBER NOT NULL,
    TITLE VARCHAR2(200) NOT NULL,
    CONTENT CLOB NOT NULL,
    ANSWER_CONTENT CLOB,
    CREATED_AT DATE DEFAULT SYSDATE NOT NULL,
    ANSWERED_AT DATE,
    DELETED_AT DATE,
    STORE_ID NUMBER NOT NULL,
    CONSTRAINT PK_INQUIRIES PRIMARY KEY (INQUIRY_ID),
    CONSTRAINT FK_INQUIRIES_STORE FOREIGN KEY (STORE_ID)
        REFERENCES STORES (STORE_ID)
);

CREATE SEQUENCE SEQ_INQ START WITH 1000 INCREMENT BY 1 NOCACHE NOCYCLE;

COMMENT ON TABLE INQUIRIES IS '문의사항';
COMMENT ON COLUMN INQUIRIES.INQUIRY_ID IS '문의사항 ID';
COMMENT ON COLUMN INQUIRIES.TITLE IS '제목';
COMMENT ON COLUMN INQUIRIES.CONTENT IS '내용';
COMMENT ON COLUMN INQUIRIES.ANSWER_CONTENT IS '답변 내용';
COMMENT ON COLUMN INQUIRIES.CREATED_AT IS '생성일시';
COMMENT ON COLUMN INQUIRIES.ANSWERED_AT IS '답변일시';
COMMENT ON COLUMN INQUIRIES.DELETED_AT IS '삭제일시';
COMMENT ON COLUMN INQUIRIES.STORE_ID IS '점포 ID';

CREATE TABLE INQUIRY_FILES (
    INQUIRY_FILE_ID NUMBER NOT NULL,
    ORIGINAL_NAME VARCHAR2(255) NOT NULL,
    STORED_NAME VARCHAR2(255) NOT NULL,
    STORAGE_PATH VARCHAR2(500) NOT NULL,
    ATTACH_TARGET CHAR(1) NOT NULL,
    CREATED_AT DATE DEFAULT SYSDATE NOT NULL,
    DELETED_AT DATE,
    INQUIRY_ID NUMBER NOT NULL,
    CONSTRAINT PK_INQUIRY_FILES PRIMARY KEY (INQUIRY_FILE_ID),
    CONSTRAINT CK_INQUIRY_FILES_ATTACH_TARGET CHECK (ATTACH_TARGET IN ('Q', 'A')),
    CONSTRAINT FK_INQUIRY_FILES_INQUIRY FOREIGN KEY (INQUIRY_ID)
        REFERENCES INQUIRIES (INQUIRY_ID)
);

CREATE SEQUENCE SEQ_INQ_FILE START WITH 1000 INCREMENT BY 1 NOCACHE NOCYCLE;

COMMENT ON TABLE INQUIRY_FILES IS '문의사항 파일';
COMMENT ON COLUMN INQUIRY_FILES.INQUIRY_FILE_ID IS '문의사항 파일 ID';
COMMENT ON COLUMN INQUIRY_FILES.ORIGINAL_NAME IS '원본 파일명';
COMMENT ON COLUMN INQUIRY_FILES.STORED_NAME IS '저장 파일명';
COMMENT ON COLUMN INQUIRY_FILES.STORAGE_PATH IS '저장 경로';
COMMENT ON COLUMN INQUIRY_FILES.CREATED_AT IS '생성일시';
COMMENT ON COLUMN INQUIRY_FILES.DELETED_AT IS '삭제일시';
COMMENT ON COLUMN INQUIRY_FILES.INQUIRY_ID IS '문의사항 ID';
